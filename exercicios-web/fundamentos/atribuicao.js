const a = 7;
let b = 3;

b += a // atribuiçao aditiva (acrecentando b = b + a)
console.log(b);

b -= 4 // atribuiçao subtrativa (retirando b = b - 4)
console.log(b);

b *= 2 // atribuiçao multiplicativa (multiplicando b = b * 2)
console.log(b);

b /= 2 // atribuiçao divisora (dividindo b = b / 2)
console.log(b);

b %= 2 // atribuiçao modular (modulando b = b % 2) resto da divisão
console.log(b)